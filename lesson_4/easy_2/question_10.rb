# What are the benefits of using Object Oriented Programming in Ruby?

# - Encapsulation: Keeps funtionality separate and hidden from other parts of code.
#     Helps keep a single change in code from breaking an entire program through a ripple
#     effect.

# - Allows levels of abstraction. Programmers can think in broader terms of nouns and 
#     verbs interacting with eachother, rather than focusing on implementation details.

# - Allows for greater felxibility

# - Polymorphism: Allows objects of different classes to respond to the same method call

Things I didn't think of:

- Functionality can be added to different parts of code without duplication.

- Applications can be built faster through the use of pre_written code.

- As a piece of software becomes more complicated, the complexity is more easily managed.